-- ── Tabelas ──────────────────────────────────────────────────────

CREATE TABLE CATEGORIASIMPACTO (
    ID           NUMBER         GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NOME         VARCHAR2(100)  NOT NULL,
    DESCRICAO    VARCHAR2(500)  DEFAULT '' NOT NULL,
    TIPODESASTRE VARCHAR2(100)  DEFAULT '' NOT NULL
);

CREATE TABLE USUARIOS (
    ID           NUMBER         GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NOME         VARCHAR2(100)  NOT NULL,
    EMAIL        VARCHAR2(200)  NOT NULL,
    SENHAHASH    VARCHAR2(255)  NOT NULL,
    PERFIL       VARCHAR2(50)   DEFAULT 'Pesquisador' NOT NULL,
    DATACADASTRO TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
    ATIVO        NUMBER(1)      DEFAULT 1 NOT NULL,
    CONSTRAINT UQ_USUARIOS_EMAIL  UNIQUE (EMAIL),
    CONSTRAINT CK_USUARIOS_ATIVO  CHECK (ATIVO IN (0,1)),
    CONSTRAINT CK_USUARIOS_PERFIL CHECK (PERFIL IN ('Administrador','Pesquisador'))
);

CREATE TABLE TECNOLOGIAS (
    ID                 NUMBER          GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NOME               VARCHAR2(200)   NOT NULL,
    DESCRICAO          VARCHAR2(1000)  DEFAULT '' NOT NULL,
    ORIGEMESPACIAL     VARCHAR2(200)   DEFAULT '' NOT NULL,
    LATITUDE           BINARY_DOUBLE   NULL,
    LONGITUDE          BINARY_DOUBLE   NULL,
    STATUS             VARCHAR2(50)    DEFAULT 'Ativo' NOT NULL,
    DATACADASTRO       TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    CATEGORIAIMPACTOID NUMBER          NOT NULL,
    CONSTRAINT FK_TEC_CATEGORIA FOREIGN KEY (CATEGORIAIMPACTOID)
        REFERENCES CATEGORIASIMPACTO(ID),
    CONSTRAINT CK_TEC_STATUS CHECK (STATUS IN ('Ativo','Monitorando','Alerta','Crítico'))
);

CREATE TABLE "EFMIGRATIONSHISTORY" (
    "MigrationId"    VARCHAR2(150) NOT NULL,
    "ProductVersion" VARCHAR2(32)  NOT NULL,
    CONSTRAINT PK_EFMigHist PRIMARY KEY ("MigrationId")
);

-- ── Índices ──────────────────────────────────────────────────────
CREATE INDEX IDX_TEC_CATEGORIA   ON TECNOLOGIAS (CATEGORIAIMPACTOID);
CREATE INDEX IDX_TEC_STATUS      ON TECNOLOGIAS (STATUS);
CREATE INDEX IDX_TEC_DATACAD     ON TECNOLOGIAS (DATACADASTRO DESC);
CREATE INDEX IDX_USU_EMAIL       ON USUARIOS (EMAIL);

