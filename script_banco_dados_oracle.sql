-- ── Views ────────────────────────────────────────────────────────
CREATE OR REPLACE VIEW VW_TECNOLOGIAS_POR_CATEGORIA AS
SELECT c.NOME AS CATEGORIA, c.TIPODESASTRE, COUNT(t.ID) AS TOTAL
FROM CATEGORIASIMPACTO c
LEFT JOIN TECNOLOGIAS t ON t.CATEGORIAIMPACTOID = c.ID
GROUP BY c.NOME, c.TIPODESASTRE;
/

CREATE OR REPLACE VIEW VW_ESTATISTICAS_DASHBOARD AS
SELECT
    (SELECT COUNT(*)                       FROM TECNOLOGIAS)             AS TOTALTECNOLOGIAS,
    (SELECT COUNT(DISTINCT ORIGEMESPACIAL) FROM TECNOLOGIAS)             AS TOTALMISSOES,
    (SELECT COUNT(*)                       FROM CATEGORIASIMPACTO)       AS TOTALSETORES,
    (SELECT COUNT(*) FROM TECNOLOGIAS WHERE STATUS IN ('Alerta','Crítico')) AS ALERTASATIVOS
FROM DUAL;
/

